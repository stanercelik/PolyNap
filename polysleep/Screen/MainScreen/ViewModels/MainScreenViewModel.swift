    /// Supabase'den aktif uyku programƒ±nƒ± ve bloklarƒ±nƒ± y√ºkler
    @MainActor
    func loadScheduleFromSupabase() async {
        isLoading = true
        errorMessage = nil
        
        // Kullanƒ±cƒ±nƒ±n oturum a√ßƒ±p a√ßmadƒ±ƒüƒ±nƒ± kontrol et
        if !authManager.isAuthenticated {
            print("PolySleep Debug: Kullanƒ±cƒ± oturumu kapalƒ±, varsayƒ±lan program kullanƒ±lƒ±yor")
            loadDefaultSchedule()
            isLoading = false
            return
        }
        
        do {
            print("PolySleep Debug: Supabase'den aktif program y√ºkleniyor...")
            
            // Aktif programƒ± getir
            let activeSchedule = try await scheduleService.getActiveSchedule()
            
            if let schedule = activeSchedule {
                print("PolySleep Debug: Aktif program bulundu, bloklar y√ºkleniyor...")
                
                // Program bloklarƒ±nƒ± getir
                let blocks = try await scheduleService.getSleepBlocksForSchedule(scheduleId: schedule.id)
                
                if !blocks.isEmpty {
                    print("PolySleep Debug: \(blocks.count) adet program bloƒüu y√ºklendi")
                    
                    // UserScheduleModel'e d√∂n√º≈üt√ºr
                    let userScheduleModel = schedule.toUserScheduleModel(with: blocks)
                    
                    // Model'i g√ºncelle
                    self.model.schedule = userScheduleModel
                    
                    // Yerel veritabanƒ±na kaydet
                    saveScheduleToLocalDatabase(userScheduleModel)
                    isLoading = false
                    return
                } else {
                    print("PolySleep Debug: Program bloklarƒ± bulunamadƒ±, varsayƒ±lan program kullanƒ±lacak")
                }
            } else {
                print("PolySleep Debug: Aktif program bulunamadƒ±, yerel veritabanƒ± kontrol ediliyor")
                
                // Yerel veritabanƒ±ndan programƒ± y√ºklemeyi dene
                loadScheduleFromLocalDatabase()
                
                // Yerel veritabanƒ±nda program yoksa ve model varsayƒ±lan programƒ± kullanƒ±yorsa
                if model.schedule.id == "default" {
                    print("PolySleep Debug: Yerel veritabanƒ±nda program bulunamadƒ±, varsayƒ±lan program Supabase'e kaydedilecek")
                    
                    // Varsayƒ±lan programƒ± Supabase'e kaydet
                    let success = try await scheduleService.saveRecommendedSchedule(
                        schedule: UserScheduleModel.defaultSchedule.toSleepScheduleModel,
                        adaptationPeriod: model.currentDay
                    )
                    
                    if success {
                        print("PolySleep Debug: Varsayƒ±lan program Supabase'e kaydedildi")
                    } else {
                        print("PolySleep Debug: Varsayƒ±lan program Supabase'e kaydedilemedi")
                    }
                }
            }
            
            isLoading = false
        } catch {
            print("PolySleep Debug: Supabase'den program y√ºklenirken hata olu≈ütu: \(error)")
            
            // Hata durumunda yerel veritabanƒ±ndan y√ºklemeyi dene
            loadScheduleFromLocalDatabase()
            
            DispatchQueue.main.async {
                self.errorMessage = NSLocalizedString("supabase.error.sync", comment: "")
                self.isLoading = false
            }
        }
    }
    
